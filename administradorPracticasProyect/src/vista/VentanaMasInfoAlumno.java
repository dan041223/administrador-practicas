    private void jLabel15MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel15MouseClicked
        FileOutputStream fos = null;
        try {
            if (cvActual!=null) {
                JnaFileChooser fileChooser = new JnaFileChooser();
                fileChooser.setMode(JnaFileChooser.Mode.Directories);
                fileChooser.showSaveDialog(this);
                File directorioElegido = fileChooser.getSelectedFile();
                if (directorioElegido != null) {
                    String rutaDirectorio = directorioElegido.getAbsolutePath();
                    String nombreArchivo = "\\CV" + tfNombre.getText() + ".pdf";
                    File curriculum = new File(rutaDirectorio + nombreArchivo);
                    fos = new FileOutputStream(curriculum);
                    fos.write(cvActual);
                    Notification notificacion = new Notification(this, Notification.Type.SUCCESS, Notification.Location.TOP_CENTER, "El archivo ha sido descargado con exito");
                    notificacion.showNotification();
                } else {
                    Notification notificacion = new Notification(this, Notification.Type.INFO, Notification.Location.TOP_CENTER, "La descarga ha sido cancelada");
                    notificacion.showNotification();
                }
            }else{
                Notification notificacion = new Notification(this, Notification.Type.WARNING, Notification.Location.TOP_CENTER, "El alumno no tiene ningun curriculum subido");
                    notificacion.showNotification();
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(VentanaMasInfoAlumno.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(VentanaMasInfoAlumno.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jLabel15MouseClicked


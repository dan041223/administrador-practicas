    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        if (alumno.getCv() != null) {
            MessageDialog mensaje = new MessageDialog(this);
            mensaje.showMessage("Este alumno ya tiene subido un curriculum", "¿Está seguro de querer sobreescribir el curriculum?");
            if (mensaje.getMessageType() == MessageDialog.MessageType.OK) {
                JnaFileChooser fileChooser = new JnaFileChooser();
                fileChooser.setMode(JnaFileChooser.Mode.Files);
                fileChooser.showOpenDialog(this);
                File cvSubido = fileChooser.getSelectedFile();
                if (cvSubido != null) {
                    try {
                        cvASubir = new FileInputStream(cvSubido);
                    } catch (FileNotFoundException ex) {
                        Logger.getLogger(VentanaMasInfoAlumno.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    Notification notificacion = new Notification(this, Notification.Type.SUCCESS, Notification.Location.TOP_CENTER, "El curriculum ha sido sustituido");
                    notificacion.showNotification();
                }else{
                    Notification notificacion = new Notification(this, Notification.Type.INFO, Notification.Location.TOP_CENTER, "La subida de curriculum ha sido cancelada");
                    notificacion.showNotification();
                }
            }else{
                Notification notificacion = new Notification(this, Notification.Type.SUCCESS, Notification.Location.TOP_CENTER, "El curriculum no ha sido sustituido");
                notificacion.showNotification();
            }
        }else{
            JnaFileChooser fileChooser = new JnaFileChooser();
            fileChooser.setMode(JnaFileChooser.Mode.Files);
            fileChooser.showOpenDialog(this);
            File cvSubido = fileChooser.getSelectedFile();
            if (cvSubido != null) {
                try {
                    FileInputStream fis = new FileInputStream(cvSubido);
                    cvASubir = fis;
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(VentanaMasInfoAlumno.class.getName()).log(Level.SEVERE, null, ex);
                }
                Notification notificacion = new Notification(this, Notification.Type.SUCCESS, Notification.Location.TOP_CENTER, "Curriculum preparado para agregar");
                notificacion.showNotification();
            }else{
                Notification notificacion = new Notification(this, Notification.Type.INFO, Notification.Location.TOP_CENTER, "La subida de curriculum ha sido cancelada");
                notificacion.showNotification();
            }
        }
        
    }//GEN-LAST:event_jLabel7MouseClicked


    private void formWindowClosed(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosed
        if (!tfBusqueda.getText().equals("Busque en cualquier campo")) {
            List<Anexo22> anexo = anexoMetodos.rellenarListaAnexo("SELECT * FROM anexo22 WHERE CAST(id AS TEXT) LIKE '%" + tfBusqueda.getText()
                + "%' "
                + "OR idCentro LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR familia_profesional LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR ciclo LIKE '%" + tfBusqueda.getText() + "%' ORDER BY id ASC");
            PanelAnexo22.tablaAnexos = anexoMetodos.rellenarTablaAnexo(PanelAnexo22.tablaAnexos, anexo);
        }else{
            List<Anexo22> anexos = anexoMetodos.rellenarListaAnexo("SELECT * FROM anexo22 where eliminado = FALSE;");
            PanelAnexo22.tablaAnexos = anexoMetodos.rellenarTablaAnexo(PanelAnexo22.tablaAnexos, anexos);
        }
    }//GEN-LAST:event_formWindowClosed


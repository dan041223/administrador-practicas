    private void formWindowClosed(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosed
        if (!tfBusqueda.getText().equals("Busque en cualquier campo")) {
            List<Practica> practica = practicaMetodos.rellenarListaPractica("SELECT * FROM practica WHERE CAST(id AS TEXT) LIKE '%" + tfBusqueda.getText()
                + "%' "
                + "OR id_alumno LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR fecha_inicio LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR fecha_fin LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR id_convenio LIKE '%" + tfBusqueda.getText() + "%' ORDER BY id ASC");
            PanelPractica.tablaPractica = practicaMetodos.rellenarTablaPractica(PanelPractica.tablaPractica, practica);
        }else{
            List<Practica> practicas = practicaMetodos.rellenarListaPractica("SELECT * FROM practica where eliminado = FALSE;");
            PanelPractica.tablaPractica = practicaMetodos.rellenarTablaPractica(PanelPractica.tablaPractica, practicas);
        }
    }//GEN-LAST:event_formWindowClosed


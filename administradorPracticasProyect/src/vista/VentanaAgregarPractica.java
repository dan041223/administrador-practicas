    private void botonAnexo8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonAnexo8MouseClicked
            JnaFileChooser fileChooser = new JnaFileChooser();
            fileChooser.setMode(JnaFileChooser.Mode.Files);
            fileChooser.showOpenDialog(this);
            File cvSubido = fileChooser.getSelectedFile();
            if (cvSubido != null) {
                try {
                    FileInputStream fis = new FileInputStream(cvSubido);
                    cvASubir1 = fis;
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(VentanaAgregarPractica.class.getName()).log(Level.SEVERE, null, ex);
                }
                Notification notificacion = new Notification(this, Notification.Type.SUCCESS, Notification.Location.TOP_CENTER, "Anexo8 preparado para agregar");
                notificacion.showNotification();
            }else{
                Notification notificacion = new Notification(this, Notification.Type.INFO, Notification.Location.TOP_CENTER, "La subida del Anexo8 ha sido cancelada");
                notificacion.showNotification();
            }
        
    }//GEN-LAST:event_botonAnexo8MouseClicked


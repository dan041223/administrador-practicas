    private void formWindowClosed(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosed
        if (!tfBusqueda.getText().equals("Busque en cualquier campo")) {
            List<Tutor> tutores = tutorMetodos.rellenarListaTutores("SELECT * FROM tutor_centro WHERE CAST(id AS TEXT) LIKE '%" + tfBusqueda.getText()
                + "%' "
                + "OR nombre LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR apellidos LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR telefono LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR email LIKE '%" + tfBusqueda.getText() + "%' "
                + "%' ORDER BY id ASC");
            PanelTutor.tablaTutor = tutorMetodos.rellenarTablaTutores(PanelTutor.tablaTutor, tutores);
        }else{
            List<Tutor> tutores = tutorMetodos.rellenarListaTutores("SELECT * FROM tutor_centro where eliminado = FALSE;");
            PanelTutor.tablaTutor = tutorMetodos.rellenarTablaTutores(PanelTutor.tablaTutor, tutores);
        }
    }//GEN-LAST:event_formWindowClosed


    private void rellenarCampos(Usuario usuario) {
        tfNombre.setText(usuario.getNombre());
        tfApellidos.setText(usuario.getApellidos());
        tfFecha_Nacimiento.setText(usuario.getFechaNacimiento().toString());
        tfPassword.setText(usuario.getPassword());
        tfEmail.setText(usuario.getEmail());
        tfTelefono.setText(usuario.getTelefono());
        if (usuario.getTipousuario() == TIPOUSUARIO.ADMINISTRADOR) {
            cbTipoUsuario.addItem("administrador");
        }else{
            cbTipoUsuario.addItem("tutor");
        }
    }

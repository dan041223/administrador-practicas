    private void formWindowClosed(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosed
        if (!tfBusqueda.getText().equalsIgnoreCase("Busque en cualquier campo")) {
            List<Usuario> usuarios = usuarioMetodos.rellenarListaUsuarios(
                "SELECT * FROM usuarios WHERE "
                + "(CAST(id AS TEXT) LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR nombre LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR apellidos LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR telefono LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR email LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR fecha_nacimiento LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR password LIKE '%" + tfBusqueda.getText() + "%' "
                + "OR CASE WHEN tipo_usuario::text ILIKE '%" + tfBusqueda.getText() + "%' THEN true ELSE false END "
                + "AND eliminado = FALSE "
                + "ORDER BY id ASC"
            );

            PanelUsuarios.tablaUsuarios = usuarioMetodos.rellenarTablaUsuarios(PanelUsuarios.tablaUsuarios, usuarios);
        }else{
            List<Usuario> usuarios = usuarioMetodos.rellenarListaUsuarios("SELECT * "
                    + "FROM usuarios "
                    + "WHERE eliminado = FALSE "
                    + "ORDER BY id ASC");
            PanelUsuarios.tablaUsuarios = usuarioMetodos.rellenarTablaUsuarios(PanelUsuarios.tablaUsuarios, usuarios);
        }
    }//GEN-LAST:event_formWindowClosed


